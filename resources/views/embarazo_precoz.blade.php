		<h2>Problemas en un embarazo precoz</h2>
			

			<p>
				Se pueden producir distintos probleas a la hora de referirnos al embarazo producido a temprana edad. Más aún si finalmente se decide seguir adelante y dar a luz.
			<p class="font-weight-light h3 ">1. Problemas durante el embarazo
			</p>
			
				Placenta previ Se trata de una complicación que como su nombre bien indica se sitúa en la placenta, haciendo que se desplace hasta llegar al cuello uterino. Este tipo de problema muestra una probabilidad de 1 entre 200 y suele aparecer en la semana número 20 de embarazo. Uno de los síntomas más comunes es un sangrado vaginal periódico que se produce de forma indolora. Podemos encontrar tres tipos de placenta previa:
				<p class="font-weight-light ">1	Placenta previa o total: Se produce cuando la placenta cubre en su totalidad el cérvix o cuello uterino.
</p>

			
			<p class="font-weight-light ">2	Placenta previa parcial: Cuando la placenta cubre el cérvix de manera parcial.
</p>
			
			<p class="font-weight-light  ">3	Placenta previa marginal: Cuando la placenta sólo cubre parcialmente el cérvix. Se trata de la manera menos invasiva.
</p>
			
			<p class="font-weight-light h3 ">Preeclampsia
</p>
				Cuando hablamos de preeclampsia, hablamos de una hipertensión de las arterias. Otro de los efectos es la proteinuria, una aparición de exceso de proteínas en la orina. Para que sea llamado preeclampsia como tal se tienen que producir los dos efectos.
				Las consecuencias pueden llegar a consumar un grave daño en los riñones, y si se esto empeora, también pueden salir perjudicados otros órganos vitales como el hígado, el cerebro o incluso la propia sangre. Finalmente, si no se llega a controlar, se podría poner en peligro la vida del futuro bebe o incluso de la mujer.

				<p class="font-weight-light h3 ">Parto prematuro
</p>
				
				Se denomina como parto prematuro el producido entre las semanas 28 y 37 de embarazo. Entre la semana 20 y 28 se conocería como parto inmaduro y si esto ocurriera antes de la semana número 20 se consideraría como un aborto.
				Se trata de un problema obstétrico y neonatal y que tiene consecuencias fatales para la mujer, entre las que encontramos infecciones en la vía urinaria, diabetes o anemias severas entre otras muchas.
				También se puede llegar al aborto o incluso anomalías uterinas como la incompetencia cervical, mientras que la malformación del feto siempre va a ser un riesgo a correr que estará presente en todo momento. Esto nos puede llevar de nuevo a la primera complicación que se trata de la placenta previa.
				<p class="font-weight-light h3 ">¿Cómo se puede observar si existen posibilidades de contraer un parto prematuro? 
</p>
				Algunos síntomas de esto se traducirán en fuertes contracciones constantes, hemorragias vaginales o una constante sensación de que el niño empuja hacia abajo como si hiciera fuerza para salir.
				<p class="font-weight-light h3 ">Anemia grave
</p>
				

				Conocemos la anemia como un tipo de infección creada en la sangre. En el embarazo adolescente es producida debido a los bajos niveles de posesión de hemoglobina y por lo tanto de hierro, ya que son portadores de este mineral de tal forma que encontremos dos tipos de anemias:
				<p class="font-weight-light h3 ">1	Anemia ferropénica:
</p>
			 Esta es producida básicamente por la falta de hierro que generalmente aparece por una alimentación inadecuada de nuestro organismo.
			<p class="font-weight-light h3 ">2	Anemia gravídica: 
</p>
			Se trata de un derivado de la enfermedad que se desarrolla en un sentido menos grave. No necesita un tratamiento médico. Esta es producida cuando aumenta el volumen sanguíneo en nuestro cuerpo a la vez que disminuye la concentración de hemoglobina.
			<p class="font-weight-light h3 ">
</p>
			Hay que señalar como dato que los niveles corrientes de hemoglobina en sangre de una mujer embaraza oscilan entre los 12-16g/dL, mientras que la no embaraza muestra un cambio con unos niveles de entre 11-14g/dL.
			Rotura prematura de agua
			Se trata de algo que afecta a un tercio de las embarazadas adolescentes. Uno de los problemas que pueden producirse es la entrada de bacterias dentro de la bolsa, ya que esta se encuentra abierta. Esto se llama amnionitis o corioamnioniti ¿La solución? Se debe provocar un parto voluntario si no se quiere que la madre o el pequeño sufran algún tipo de problema realmente grave.
			Como curiosidad, en ocasiones puede producirse un parto velado, donde curiosamente el bebé nace con su bolsa amniótica en perfectas condiciones. Hay que señalar que este tipo de parto no conlleva a ningún tipo de problema de salud ni para la madre ni para el recién nacido. </p>
