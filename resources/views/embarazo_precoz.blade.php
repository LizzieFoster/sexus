			<h2>Problemas tras el embarazo</h2>
			<p>

			•	Se puede llegar a producir una falta de atención médica de un ginecólogo que ayuda a normalizar y regularizar las visitas por motivos tales como la vergüenza o directamente la desinformación.
						<p class="font-weight-light h3 ">
</p>
			•	Se corre un mayor riesgo de no acudir a las visitas ginecológicas, o no seguir los consejos del médico debido a la poca experiencia o madurez de la joven madre.
						<p class="font-weight-light h3 ">
</p>
			•	El bebé puede presentar altas posibilidades de nacer con un peso por debajo de lo normal y con depósitos de grasas bajo mínimos, acarreándole multitud de enfermedades.
						<p class="font-weight-light h3 ">
</p>
			•	La alta mortalidad supone un problema tras el embarazo, sobre todo en países en vías de desarrollo.
						<p class="font-weight-light h3 ">
</p>
			•	Para el recién nacido, también puede suponer un problemas que hace que nazcan con Espina Bífida o el Síndrome de muerte súbita del lactante (se trata de la muerte repentina de un niño que aparentemente se mostraba sano).
						<p class="font-weight-light h3 ">
</p>
			•	Se puede llegar a crear un rechazo al bebe debido a la falta de responsabilidad al no querer asumir el nuevo rol de ser madre.

						<p class="font-weight-light h3 ">Consecuencias del embarazo precoz
</p>
				Las consecuencias pueden producirse principalmente a niveles psicológicos que afectan a la moralidad de la mujer (o a la pareja en general) o a niveles sociales donde la discriminación o los abandonos se tornan en problemas bastante comunes.
				<p class="font-weight-light h3 ">Psicológicas
</p>
				

			1	Depresión: Se trata de una enfermedad que puede llegar a desarrollarse en caso de angustia y agobio a la hora enfrentarnos a las nuevas responsabilidades y habernos concienciado de lo que se ha perdido anteriormente.
			<p class="font-weight-light h3 ">
</p>
			2	Sensación de frustración: La falta de medios es la principal causa de una sensación de frustración con vistas al futuro.
			<p class="font-weight-light h3 ">
</p>
			3	Preocupaciones obsesivas por el bienestar del feto o el bebé recién nacido: La falta de conocimientos y enfrentarnos a algo nuevo puede hacer que se lleguen a puntos obsesivos por el bienestar del bebé o el feto. Igualmente, esta consecuencia se puede producir también en embarazos no adolescentes.
			<p class="font-weight-light h3 ">
</p>
			4	Sensación de culpabilidad: Esto puede suceder cuando se ha parido en contra de la voluntad y sentimos que nos hemos fallado a nosotros mismos, o directamente a nuestros familiares más cercanos.
			<p class="font-weight-light h3 ">Sociales
</p>
				
			1	Matrimonio obligado: En el caso de que no se estuviera casado, en ocasiones se puede llegar a la consumación de un matrimonio obligado debido a la noticia de un nacimiento prematuro.
			<p class="font-weight-light h3 ">
</p>
			2	Abandono del padre: En ocasiones aparecen problemas relacionados con la pareja donde la otra persona, al conocer que va a ser padre, se resigna y decide abandonar a la madre cuando aún no ha dado a luz a su suerte.<p class="font-weight-light h3 ">
</p>
			3	Rechazo por parte de familiares o gente cercana: Hay otro tipo de problemas sociales que hacen que la familia o amigos discrimine a la chica debido al problema que puede suponer en muchos casos la carga de ser madre de un crio.<p class="font-weight-light h3 ">
</p>
			4	Abandono de los estudios: Engendrar y cuidar a un bebé requiere de muchísimo tiempo. Esto hace que los encargados de cuidarlo tengan que renunciar a multitud de prácticas que antes se solían hacer y a las que se deben renunciar. Entre ellas encontramos principalmente el abandono de los estudios, algo muy común sobre todo en el periodo perteneciente a la Enseñanza Secundaria Obligatoria, donde la tasa de abandono en nuestro país se trata de una de las más altas de toda Europa.<p class="font-weight-light h3 ">
</p>
			5	Menor probabilidad de encontrar una ocupación laboral: Actualmente, la situación laboral hace que se contraten perfiles estables y que dispongan de suficiente tiempo libre, algo que no es posible teniendo a cargo un hijo recién nacido. Si le añadimos la falta de formación académica debido al abandono escolar hay que señalar que se trata de un trabajo difícil el encontrar una estabilidad laboral.</p></div>

