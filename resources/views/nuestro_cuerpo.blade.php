*Cuerpo esponjoso	<p class="font-weight-light ">
		</p>

	El cuerpo esponjoso es la más pequeña de las tres columnas de tejido eréctil que se encuentran en el interior del pene (las otras dos son los cuerpos cavernosos). Está ubicado en la parte inferior del miembro viril. El glande es la última porción y la parte más ancha del cuerpo esponjoso; presenta una forma cónica.	<p class="font-weight-light ">
