*Uretra	<p class="font-weight-light ">
		</p>

	La uretra es el conducto por el que discurre la orina desde la vejiga urinaria hasta el exterior del cuerpo durante la micción. La función de la uretra es excretora en ambos sexos y también cumple una función reproductiva en el hombre al permitir el paso del semen desde las vesículas seminales que abocan a la próstata hasta el exterior.	<p class="font-weight-light ">
		</p>

*Glándulas bulbouretrales	<p class="font-weight-light ">
		</p>

	Las glándulas bulbouretrales, también conocidas como glándulas de Cowper, son dos glándulas que se encuentran debajo de la próstata. Su función es secretar un líquido alcalino que lubrica y neutraliza la acidez de la uretra antes del paso del semen en la eyaculación. Este líquido puede contener espermatozoides (generalmente arrastrados), por lo cual la práctica de retirar el pene de la vagina antes de la eyaculación no es un método anticonceptivo efectivo.	<p class="font-weight-light ">
