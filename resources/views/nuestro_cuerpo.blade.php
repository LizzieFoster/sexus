*Cuerpo cavernoso	<p class="font-weight-light ">
		</p>

	Los cuerpos cavernosos constituyen un par de columnas de tejido eréctil situadas en la parte superior del pene, que se llenan de sangre durante las erecciones.	<p class="font-weight-light ">
		</p>

*Epidídimo	<p class="font-weight-light ">
		</p>

	Está constituido por la reunión y apelotonamiento de los conductos seminíferos. Se distingue una cabeza, cuerpo y cola que continúa con el conducto deferente. Tiene aproximadamente 5 cm de longitud por 12 mm de ancho. Está presente en todos los mamíferos machos.	<p class="font-weight-light ">
