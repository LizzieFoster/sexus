*Conducto deferente	<p class="font-weight-light ">
		</p>

	Los conductos deferentes son un par de conductos rodeados de músculo liso, cada uno de 30 cm de largo aproximadamente, que conectan el epidídimo con los conductos eyaculatorios, intermediando el recorrido del semen entre éstos.	<p class="font-weight-light ">
