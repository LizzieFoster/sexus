*Testículos	<p class="font-weight-light ">
		</p>

	Son los principales órganos del sistema reproductor masculino. Produce las células espermáticas y las hormonas sexuales masculinas. Se encuentran alojados en el escroto o saco escrotal que es un conjunto de envolturas que cubre y aloja a los testículos en el varón.	<p class="font-weight-light ">
