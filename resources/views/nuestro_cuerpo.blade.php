¿Qué función desempeña la vagina?	<p class="font-weight-light ">
		</p>

	La vagina es un conducto de 10 a 15 cm de largo que comunica el útero con el exterior. Este órgano, sumamente elástico, sirve de receptáculo al pene y a los espermatozoides y contituyen la vía de salida del bebé cuando nace. Las paredes de la vagina, formadas por músculo y tejido conjuntivo fibroelástico, están normalmente plegadas hacia adentro, pero pueden distenderse dejando un espacio interno de 10 cm o más de diámetro, lo suficientemente amplio para dar paso a un bebé.	<p class="font-weight-light ">
