	La secreción de las glándulas de Bartholin, que están situadas a uno y otros lados de la abertura vaginal, y el moco que produce el cuello del útero mantienen húmedos los genitales externos y la vagina. Cada mes, durante la ovulación, estas secreciones aumentan y se hacen más fluidas, lo que ayuda a los espermatozoides a desplazarse a través de la vagina y del útero para alcanzar las trompas de Falopio, que es donde se efectúa la fecundación. Durante el resto del ciclo, el moco es más denso y difícil de penetrar.	<p class="font-weight-light ">
		</p>

	La vagina se encuentra por detrás de la vejiga urinaria y de la uretra, delante del recto. Al nacer, la abertura externa está total o parcialmente cubierta por una delgada membrana mucosa, el himen, que termina rompiéndose durante la primera relación sexual e incluso antes, al hacer ejercicio o cualquier otra actividad más o menos enérgica.	<p class="font-weight-light ">
