*Próstata	<p class="font-weight-light ">
		</p>

	La próstata es un órgano glandular del aparato genitourinario, exclusivo de los hombres, con forma de castaña, localizada enfrente del recto, debajo y a la salida de la vejiga urinaria. Contiene células que producen parte del líquido seminal que protege y nutre a los espermatozoides contenidos en el semen.	<p class="font-weight-light ">
