		</p>

¿Qué estructura tiene el útero?	<p class="font-weight-light ">
		</p>

	El útero o matriz es el órgano donde se implanta el óvulo fecundado, allí recibe protección y sustento durante los nueve meses que tarda en desarrollarse como un nuevo ser humano. El útero está situado detrás de la vejiga urinaria; en una mujer no embarazada tiene la forma y el tamaño de una pera invertida; mide, aproximadamente, 8 cm de largo y 5 de ancho en la parte superior, que constituye el cuerpo uterino; el extremo inferior, más angosto, se llama cuello y conduce a la vagina.	<p class="font-weight-light ">
		</p>

*Vagina	<p class="font-weight-light ">
