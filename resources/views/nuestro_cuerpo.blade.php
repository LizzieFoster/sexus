Órganos internos	<p class="font-weight-light ">
		</p>

*Ovarios	<p class="font-weight-light ">
		</p>

	Son los órganos productores de gametos femeninos u ovocitos, de tamaño variado, según la cavidad, y la edad; a diferencia de los testículos, están situados en la cavidad abdominal. El proceso de formación de los óvulos, o gametos femeninos, se llama ovulogénesis y se realiza en unas cavidades o folículos cuyas paredes están cubiertas de células que protegen y nutren el óvulo. Cada folículo contiene un solo óvulo, que madura cada 28 días, aproximadamente. La ovulogénesis es periódica, a diferencia de la espermatogénesis, que es continua.	<p class="font-weight-light ">
		</p>

	Los ovarios también producen estrógenos y progesteronas, hormonas que regulan el desarrollo de los caracteres sexuales secundarios, como la aparición de vello o el desarrollo de las mamas, y preparan el organismo para un posible embarazo.	<p class="font-weight-light ">
		</p>

¿Cuántos óvulos contienen los ovarios?	<p class="font-weight-light ">
		</p>

	Para asegurar la supervivencia de la raza humana, la naturaleza es pródiga en la dotación de células reproductoras.Cuando una niña nace, lleva en sus ovarios alrededor de 2 millones de células germinales, que son óvulos en potencia. Unas tres cuartas partes degeneran antes de la pubertad, y de los cientos de miles que quedan sólo 400 ó 500 llegan a convertirse en óvulos maduros. Todos los meses, desde la pubertad hasta la menopausia, un ovario o el otro deja en libertad un óvulo listo para ser fecundado.

¿Cómo se produce la ovulación?	<p class="font-weight-light ">
		</p>

	Al llegar a la pubertad, una jovencita cuenta con miles de óvulos potenciales acumulados en la capa externa de los ovarios, o capa germinativa. Mediante un proceso que se conoce como ovogénesis, todos los meses comienzan a madurar varios óvulos, pero, excepto en contados casos, sólo uno alcanza la madurez completa. Este óvulo llega a la superficie del ovario envuelto en lo que se llama folículo de Graaf. A mediados del ciclo menstrual se efectúa la ovulación: el folículo se llena de líquido, se distiende y termina por romperse dejando caer el óvulo que contenía a la cavidad peritoneal, de donde pasa en seguida a la trompa de Falopio del lado correspondiente.	<p class="font-weight-light ">
		</p>

*Trompas de Falopio	<p class="font-weight-light ">
		</p>

	Conductos de entre 10 a 13 cm que comunican los ovarios con el útero y tienen como función llevar el óvulo hasta él para que se produzca la fecundación. En raras ocasiones el embrión se puede desarrollar en una de las trompas, produciéndose un embarazo ectópico. El orificio de apertura de la trompa al útero se llama ostium tubárico.	<p class="font-weight-light ">
		</p>

