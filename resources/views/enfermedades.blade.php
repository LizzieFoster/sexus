	<div id="section1" class="h3"> 
	Enfermedades de transmision sexual
	</div>
	Las Enfermedades de Transmisión Sexual como la gonorrea o el SIDA, pueden surgir cuando se tiene relaciones sexuales sin condón, ya sea a través del contacto íntimo vaginal, anal u oral. Sin embargo, las probabilidades de contagio aumentan cuando se tiene varias parejas al mismo tiempo, y estas enfermedades afectan por igual a hombres y mujeres de todas las edades.<p class="font-weight-light ">
		</p>

	Generalmente, estas infecciones causan síntomas que afectan los órganos genitales como dolor, enrojecimiento, pequeñas heridas, flujo, hinchazón, dificultad para orinar o dolor durante el contacto íntimo y, para identificar la enfermedad correcta, es necesario acudir al ginecólogo o al urólogo para realizar los exámenes que sean necesarios. <p class="font-weight-light ">
		</p>

	Para el tratamiento, normalmente, el médico indica el uso de antibióticos o antimicóticos en forma de comprimido o pomadas, ya que generalmente las ETS tienen cura, exceptuando el SIDA y el herpes.<p class="font-weight-light ">
		</p>
<div id="section2" class="h3">
¿Qué son las infecciones de transmisión sexual?
</div>

	Son infecciones que se transmiten de una persona a otra durante una relación sexual. Pueden ser producto de más de treinta tipos de virus, bacterias y parásitos. Las más frecuentes son: la sífilis, la gonorrea, la clamidia, el Virus del Papiloma Humano (VPH), las hepatitis B y C, el VIH. Afectan a todas las personas: varones, mujeres de cualquier edad y orientación sexual.<p class="font-weight-light ">
		</p>
<div id="section3" class="h3">
¿Cómo se transmiten?</div>

	De una persona a otra durante una relación sexual vaginal, anal u oral sin protección. Las ITS se pueden transmitir por sangre, semen, líquido preseminal o fluido vaginal.<p class="font-weight-light ">
		</p>
		<div id="section0" class="h3">
¿Cuáles son los síntomas?</div>

	Los Principales síntomas de las ITS son:<p class="font-weight-light ">
		</p>

•	Lastimaduras en la zona genital, con o sin dolor.<p class="font-weight-light ">
		</p>
•	Secreciones de pus en los genitales (vagina y pene) o en el ano.<p class="font-weight-light ">
		</p>
•	Ardor al orinar.<p class="font-weight-light ">
		</p>
•	Flujo genital u anal diferente al habitual.<p class="font-weight-light ">
		</p>
•	Dolor en la parte baja del abdomen.<p class="font-weight-light ">
		</p>
•	Lesiones en la boca o manchas en la piel.<p class="font-weight-light ">
		</p>
•	Verrugas genitales.<p class="font-weight-light ">
		</p>

Ante alguno de estos síntomas es importante hacer una consulta en el hospital o Centro de Salud.<p class="font-weight-light ">
		</p>
<div id="section4" class="h3">
¿Cómo se detecta?</div>

	Las ITS pueden no mostrar síntomas. Algunas veces solo se detectan con un examen médico como análisis de sangre u orina. En caso de embarazo, es importante que ambos padres se realicen los análisis para poder tratarse, de ser necesario, y evitar la transmisión al bebé.<p class="font-weight-light ">
		</p>
<div id="section5" class="h3">
¿Cómo se tratan?</div>
	La mayoría de estas infecciones son tratables, y muchas de ellas pueden curarse. Si no se tratan pueden producir, de acuerdo al tipo de infección, diferentes problemas de salud:<p class="font-weight-light ">
		</p>

•	Infertilidad tanto en hombres como en mujeres.<p class="font-weight-light ">
		</p>
•	Dolor crónico en la pelvis.<p class="font-weight-light ">
		</p>
•	Predisposición o mayor riesgo de tener embarazo ectópico (en una trompa).<p class="font-weight-light ">
		</p>
•	Problemas neurológicos.<p class="font-weight-light ">
		</p>
•	Pueden aumentar la posibilidad de adquirir VIH.<p class="font-weight-light ">
		</p>
•	El VPH no tratado puede relacionarse con algún tipo de cáncer.<p class="font-weight-light ">
		</p>
•	Algunas pueden pasar al bebé durante el embarazo, parto o cuando se amamanta.<p class="font-weight-light ">
		</p>
•	Algunas pueden presentar cuadros generalizados (cuyas consecuencias se expresan en todo el cuerpo).<p class="font-weight-light ">
		</p>
<div id="section6" class="h3">
¿Cómo se previenen?</div>

•	La mayoría de las ITS se pueden prevenir usando preservativo (masculino o femenino) o campo de látex desde el comienzo y durante toda la relación sexual.<p class="font-weight-light ">
		</p>


<div id="section7" class="h3">
Enfermedades de Transmisión Sexual</div>

	 A continuación indicamos los síntomas y los tratamientos de todas las ETS, también llamadas Enfermedades de Transmisión Sexual o enfermedades venéreas: <p class="font-weight-light ">
		</p>

1. Clamidia<p class="font-weight-light ">
		</p>

	La clamidia puede causar síntomas como flujo amarillento y espeso, enrojecimiento de los órganos genitales, dolor en la pelvis y durante el contacto íntimo, sin embargo en muchos casos la enfermedad no causa síntomas y la infección pasa por desapercibida. <p class="font-weight-light ">
		</p>

	La enfermedad es causada por una bacteria a través el contacto íntimo desprotegido o al compartir juguetes sexuales. <p class="font-weight-light ">
		</p>

	Cómo tratar: normalmente, el tratamiento es hecho con antibióticos como Azitromicina o Doxiciclina. Sepa más detalles sobre la clamidia.<p class="font-weight-light ">
		</p>

2. Gonorrea<p class="font-weight-light ">
		</p>

	La gonorrea es una enfermedad causada por bacterias, las cuales pueden surgir tanto en hombres como en mujeres, y es transmitida a través del contacto íntimo sin protección o al compartir juguetes sexuales.<p class="font-weight-light ">
		</p>

	La bacteria puede causar dolor al orinar, flujo amarillento semejante al pus, hemorragia vaginal fuera de la menstruación, dolor abdominal, bolitas rojas en la boca o dolor durante el contacto íntimo. Conozca más sobre los síntomas de gonorrea.<p class="font-weight-light ">
		</p>

	Cómo tratar: el tratamiento debe ser hecho con el uso de antibióticos como Ceftriaxona y Azitromicina, en caso de que no se haga el tratamiento puede llegar a afectar las articulaciones y la sangre, pudiendo poner en riesgo la vida del individuo.<p class="font-weight-light ">
		</p>

3. VPH - Verrugas genitales<p class="font-weight-light ">
		</p>

	Esta infección es causada por el virus del papiloma humano (VPH), que promueve el crecimiento de lesiones en la piel llamadas verrugas genitales que aparecen tanto en los genitales masculinos como femeninos, las cuales pueden tener textura suave o rugosa, el color puede variar dependiendo del tono de piel y no causan dolor pero son contagiosas.<p class="font-weight-light ">
		</p>

	Cómo tratar: las verrugas genitales no tienen cura, ya que el virus del VPH permanece adormecido en el cuerpo, pero existe tratamiento con la aplicación de pomadas como Aldara o Wortec sobre las verrugas. Las crisis pueden surgir por situaciones como por ejemplo un consumo excesivo de alcohol, cansancio elevado y estrés. Vea cómo es tratamiento del VPH.<p class="font-weight-light ">
		</p>

4. Herpes genital<p class="font-weight-light ">
		</p>

	El herpes genital es una enfermedad que se transmite fácilmente, es causada por el mismo virus del herpes labial y provoca pequeñas bolitas rojas en la piel muy próximas unas de otras, las cuales contienen un líquido rico en virus, de color amarillento, también puede causar enrojecimiento alrededor de ellas y picazón, afectando principalmente los muslos, el ano y los órganos genitales. Además de esto, pueden causar fiebre, dolor al orinar y flujo en el caso de la mujer. Conoce todos los síntomas que el herpes genital puede causar en Cómo identificar los síntomas del Herpes Genital.<p class="font-weight-light ">
		</p>

	Cómo tratar: el tratamiento debe ser hecho con medicamentos como Aciclovir, Valaciclovir o Famciclovir, ayudando a disminuir el malestar causado por los síntomas, ya que la infección no tiene cura y los síntomas pueden demorar hasta 20 días en desaparecer.  Conozca otras estrategias naturales para tratar para el herpes genital.<p class="font-weight-light ">
		</p>

5. Tricomoniasis<p class="font-weight-light ">
		</p>

	La tricomoniasis es causada por un parásito que causa síntomas como flujo grisáceo o verde- amarillento y espumoso con un olor fuerte y desagradable, además de que puede causar enrojecimiento, picazón intensa e hinchazón de los órganos genitales. Vea cómo distinguir los síntomas de tricomoniasis en el hombre y en la mujer.<p class="font-weight-light ">
		</p>

	La infección no es muy común, sin embargo puede ser transmitida al compartir toallas húmedas, el baño o el uso de jacuzzi y el tratamiento es hecho con antibióticos. <p class="font-weight-light ">
		</p>

	Cómo tratar: generalmente el tratamiento de esta infección es hecho con el uso de antibióticos como Metronidazol o Tioconazol por 5 a 7 días. En caso que el tratamiento no se haga hay más probabilidades de desarrollar otras infecciones, tener parto prematuro o desarrollar prostatitis. <p class="font-weight-light ">
		</p>

6. Sífilis<p class="font-weight-light ">
		</p>
	La sífilis es una enfermedad que provoca heridas y manchas rojas en las manos y pies que no sangran y no causan dolor, además de esto también pueden provocar ceguera, parálisis y problemas cardíacos. La transmisión se puede dar, además del contacto íntimo, por transfusión de sanguínea y al compartir de jeringas o agujas contaminadas y, los primeros síntomas aparecen de 3 a 12 semanas después del contagio. Vea más síntomas de sífilis.<p class="font-weight-light ">
		</p>

	Cómo tratar: el tratamiento es hecho con medicamentos como Penicilina G o eritromicina y, cuando se hace correctamente existen probabilidades de curarse.<p class="font-weight-light ">
		</p>

7. SIDA<p class="font-weight-light ">
		</p>

	El SIDA provoca síntomas como fiebre, sudoración, dolor de cabeza, sensibilidad a la luz, dolor de garganta, vómitos y diarrea. Esta enfermedad no tiene cura, apenas tratamiento para disminuir los síntomas, aumentando el tiempo y la calidad de vida. Vea más sobre los síntomas del SIDA. <p class="font-weight-light ">
		</p>

	Cómo tratar: el tratamiento es hecho con medicamentos antirretrovirales como por ejemplo Zidovudina o Lamivudina. Estos medicamentos combaten el virus y fortalecen el sistema inmune, pero no curan la enfermedad.<p class="font-weight-light ">
		</p>

 8. Mycoplasma genitalium<p class="font-weight-light ">
		</p>

	El Mycoplasma genitalium es una ETS que provoca síntomas como la aparición de flujo en el pene del hombre y de sangrados fuera del período menstrual en las mujeres. Además de inflamar la uretra causando uretritis. Conozca más sobre esta enfermedad aquí. <p class="font-weight-light ">
		</p>

	Cómo tratar: El Mycoplasma genitalium puede ser un poco difícil de tratar, sin embargo, el médico generalmente inicia el tratamiento con Azitromicina y, en caso de no funcionar, prueba con otros antibióticos como Moxifloxacino.  <p class="font-weight-light ">
		</p>

Cómo saber si tengo una ETS<p class="font-weight-light ">
		</p>

	El diagnóstico de una ETS puede ser hecho en base a los síntomas y a la observación de los órganos genitales, siendo confirmados a través de exámenes como por ejemplo el papanicolau y la prueba de Schiller.<p class="font-weight-light ">
		</p>

	Además de esto, el médico puede indicar un examen de sangre para hacer el diagnóstico de la enfermedad e indicar el tratamiento más adecuado. <p class="font-weight-light ">
		</p>

	Cuándo es necesario repetir los exámenes<p class="font-weight-light ">
		</p>
Cuando la mujer o el hombre tiene una ETS, el médico recomienda la realización de exámenes médicos cada 6 meses durante 2 años, hasta que el resultado de 3 exámenes seguidos sea negativo. <p class="font-weight-light ">
		</p>

	Durant	e la fase del tratamiento puede ser necesario ir al médico varias veces por mes para ajustar el tratamiento y curar la enfermedad, en caso de que esto sea posible.<p class="font-weight-light ">
		</p>

Cómo se transmiten las ETS<p class="font-weight-light ">
		</p>

	Las ETS además de ser transmitidas por el contacto sexual sin protección, pueden ser transmitidas:<p class="font-weight-light ">
		</p>

•	De madre a hijo a través de la sangre durante el embarazo, por la lactancia materna o durante el parto;<p class="font-weight-light ">
		</p>
•	Compartir jeringas;<p class="font-weight-light ">
		</p>
•	Compartir objetos personales como toallas, jabones, entre otros.<p class="font-weight-light ">
		</p>
•	En algunos casos, muy raros, el desarrollo de la enfermedad puede ocurrir a través de la transfusión sanguínea. <p class="font-weight-light ">
		</p>

¿Cómo evitar una ETS?<p class="font-weight-light ">
		</p>

	La mejor forma de evitar contaminarse es usando condón en todas las relaciones, ya sea que se tenga contacto íntimo vaginal, anal u oral, ya que el contacto con secreciones o con la piel puede transmitir la enfermedad. Sin embargo, es fundamental colocarse el condón correctamente antes de cualquier contacto, vea cómo hacerlo aquí.<p class="font-weight-light ">
		</p>

¿Qué puede ocurrir si no se hace el tratamiento?
<p class="font-weight-light ">
		</p>
	Cuando una ETS no son tratadas correctamente pueden surgir problemas más graves como cáncer de útero, infertilidad, problemas cardíacos, meningitis, aborto o malformaciones en el feto en caso de un embarazo.<p class="font-weight-light ">
		</p>













