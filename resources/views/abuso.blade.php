			<h1>Maltrato infantil</h1>
			El maltrato infantil se define como los abusos y la desatención de que son objeto los menores de 18 años, e incluye todos los tipos de maltrato físico o psicológico, abuso sexual, desatención, negligencia y explotación comercial o de otro tipo que causen o puedan causar un daño a la salud, desarrollo o dignidad del niño, o poner en peligro su supervivencia, en el contexto de una relación de responsabilidad, confianza o poder. La exposición a la violencia de pareja también se incluye a veces entre las formas de maltrato infantil.</div>
					<p class="font-weight-light ">
		</p>

				El maltrato infantil es una causa de sufrimiento para los niños y las familias, y puede tener consecuencias a largo plazo. El maltrato causa estrés y se asocia a trastornos del desarrollo cerebral temprano. Los casos extremos de estrés pueden alterar el desarrollo de los sistemas nervioso e inmunitario. En consecuencia, los adultos que han sufrido maltrato en la infancia corren mayor riesgo de sufrir problemas conductuales, físicos y mentales.
						<p class="font-weight-light ">
		</p>
				<div id="section2"> 
			
					<p class="font-weight-light h3">¿Qué se entiende por maltrato?
		</p>

			Un niño, según la Ley considerando como tal a todo menor de 18 años, es maltratado o abusado cuando su salud física o mental o su seguridad está en peligro, ya sea por acciones u omisiones llevadas a cabo por la madre o el padre u otras personas responsables de su cuidado, ha sufrido daños (heridas) o riesgo de daño (peligrosidad) como resultado de haber sido golpeado con la mano, pateado, zarandeado, empujado, quemado, apuñalado o pegado por uno de los padres o figuras que ejerzan ese rol; produciéndose entonces el maltrato por acción, omisión o negligencia.		<p class="font-weight-light ">
		</p> </div>
			<div id="section3"> 
				<p class="font-weight-light h3">Tipos de maltrato infantil	
		</p> </div>

			Maltrato físico: es cualquier acción no accidental efectuada por parte de los padres, cuidadores u otras personas que pueda provocar daño físico o enfermedad al menor.		<p class="font-weight-light ">
		</p>

			Abuso sexual: cualquier clase de contacto o actividad sexual con un menor realizado desde una posición de poder o autoridad sobre aquel.		<p class="font-weight-light ">
		</p>

			Abuso emocional: emocional incluye abuso verbal, castigo cruel no físico (ej, estar atado) o amenazas de maltrato.
		<p class="font-weight-light ">
		</p>
			Maltrato emocional: cualquier conducta psíquicamente destructiva de tipo intencional por parte del adulto, que cause o pueda causar deterioro en el  desarrollo emocional, social e intelectual del niño.
		<p class="font-weight-light ">
		</p>
			Negligencia en el cuidado físico y/o psicoafectivo: se refiere al daño como resultado de una falta de cuidados básicos, supervisión o atención que afecte al desarrollo físico, emocional y/o intelectual del niño, por parte de los padres o cuidadores. El máximo grado de negligencia es el abandono.
		<p class="font-weight-light ">
		</p>
			 Maltrato prenatal: toda conducta que influye negativa y patológicamente en una
			embarazada y por lo tanto en el feto, ya sea por parte de la propia gestante o 
			causada por otras personas.
		<p class="font-weight-light ">
		</p>
			Maltrato institucional: cualquier legislación, programa o procedimiento, ya sea por
			actuación u omisión, procedente de los poderes públicos y/o privados, que vulnere
			o pueda vulnerar los derechos básicos del menor, con o sin contacto directo con el
			niño. 
		<p class="font-weight-light ">
		</p>
